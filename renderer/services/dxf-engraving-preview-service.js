  // Lays out a text label as SVG using the embedded font data above. Scales the
  // character height to fit inside the part's bounding box and switches between
  // the filled-outline style and the lightweight stroke-only style via `style`.
  function buildPreviewLabelSvg(text, bbox, color, style, outerPolygon = null, holes = []) {
    if (!bbox || !Number.isFinite(bbox.w) || !Number.isFinite(bbox.h)) return '';
    // Apply style-driven text transforms here before layout so the engraving
    // size is calculated against the actual rendered characters.
    const engravedText = engravingLabelText(text, style);
    if (!engravedText) return '';
    // The remainder of the function only cares about how to *draw* each
    // glyph — translate the content-truncating modes into the visual style
    // they should be rendered with.
    const visualStyle = engravingVisualStyle(style);
    const fallbackOuter = [
      { x: 0, y: 0 },
      { x: bbox.w, y: 0 },
      { x: bbox.w, y: bbox.h },
      { x: 0, y: bbox.h },
    ];
    const layout = typeof layoutEngravingLabel === 'function'
      ? layoutEngravingLabel({
        text: engravedText,
        outerPolygon: Array.isArray(outerPolygon) && outerPolygon.length >= 3 ? outerPolygon : fallbackOuter,
        holes,
      })
      : null;
    if (!layout) return '';
    const { chars, charH, charW, startX, baseY } = layout;
    const parts = [];

    const lineSvg = (a, b, ox, width, opacity = 0.96) =>
      `<line x1="${f(ox + a[0] * charW)}" y1="${f(baseY + a[1] * charH)}" x2="${f(ox + b[0] * charW)}" y2="${f(baseY + b[1] * charH)}" stroke="${color}" stroke-width="${f(width)}" stroke-linecap="round" stroke-linejoin="round" opacity="${opacity}" pointer-events="none"/>`;

    const pathSvg = (loop, ox, width, opacity = 0.92) => {
      if (!Array.isArray(loop) || loop.length < 2) return '';
      const d = loop.map((point, index) => `${index === 0 ? 'M' : 'L'} ${f(ox + point[0] * charW)} ${f(baseY + point[1] * charH)}`).join(' ') + ' Z';
      return `<path d="${d}" fill="none" stroke="${color}" stroke-width="${f(width)}" stroke-linecap="round" stroke-linejoin="round" opacity="${opacity}" pointer-events="none"/>`;
    };

    chars.forEach((ch, index) => {
      const ox = startX + index * charW * ENGRAVING_LAYOUT_DEFAULTS.charAdvance;
      if (visualStyle === 'stroked') {
        const loops = LABEL_OUTLINE_FONT[ch];
        if (Array.isArray(loops) && loops.length) {
          loops.forEach(loop => {
            const svg = pathSvg(loop, ox, Math.max(0.8, charH * 0.06), 0.92);
            if (svg) parts.push(svg);
          });
          return;
        }
      }
      const strokes = LABEL_STROKE_FONT[ch] || [];
      const strokeWidth = visualStyle === 'simple' ? Math.max(0.8, charH * 0.055) : Math.max(0.8, charH * 0.07);
      strokes.forEach(([a, b]) => parts.push(lineSvg(a, b, ox, strokeWidth, 0.96)));
    });

    return parts.join('\n');
  }

